        /// </summary>
        public string Result;

        /// <summary>
        /// The status.
        /// </summary>
        public string Status;

        /// <summary>
        /// The sequence type.
        /// </summary>
        public string SequenceType;

        /// <summary>
        /// The group.
        /// </summary>
        public string Group;
    }
}
